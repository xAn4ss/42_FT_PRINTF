#include "ft_printf.h"

void	ft_put_int(long long n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n > 9)
	{
		ft_put_int((n / 10));
		ft_putchar((n % 10) + 48);
	}
	else
		ft_putchar(n + 48);
}
